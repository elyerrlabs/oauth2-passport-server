use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;
use Illuminate\Notifications\Notification;

class CheckoutNotification extends Notification implements ShouldQueue
{
    use Queueable;

    public $url;

    /**
     * Create a new notification instance.
     */
    public function __construct(string $url)
    {
        $this->url = $url;
    }

    /**
     * Get the notification's delivery channels.
     *
     * @return array<int, string>
     */
    public function via(object $notifiable): array
    {
        return ['mail'];
    }

    /**
     * Get the mail representation of the notification.
     */
    public function toMail(object $notifiable): MailMessage
    {
        app()->setLocale($notifiable->lang);

        return (new MailMessage)
            ->subject(__('Purchase Confirmation'))
            ->greeting(__('Hello :name', ['name' => $notifiable->name]))
            ->line(__('We have successfully received your order.'))
            ->line(__('Your purchase has been confirmed and is being processed.'))
            ->action(__('View your order'), $this->url)
            ->line(__('Thank you for shopping with us!'));
    }

    /**
     * Get the array representation of the notification.
     *
     * @return array<string, mixed>
     */
    public function toArray(object $notifiable): array
    {
        return [
            'title' => __('Order Successfully Received'),
            'message' => __('Your order was received and is being processed.'),
            'url' => $this->url,
        ];
    }

}
